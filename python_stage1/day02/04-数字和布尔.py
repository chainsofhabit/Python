#1.int(整型)
#所有的整数都是整型，python3整型只有int，Python2中有int和long
num1 = 10
num2 = - 100
#内置函数:type() --获取括号内数据类型
print(type(num1),type(num2))
#2.float(浮点型)
#所有带小数点的数都是浮点型，Python中的浮点型只有float
num3 = 0.12
num4 = 10.2
num5 = 2e2
num6 = -3.14
print(type(num6),type(num5))

# 3.bool(布尔)
#布尔就True和False  True-1 False - 0
bool1 = True
bool2 = False
print(type(bool1),type(bool2))
