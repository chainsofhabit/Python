#3.逻辑运算符:and(与),or(或),not(非)
#逻辑运算符操作的数据是布尔值 返回的结果也是布尔值
#and(与)-- 和，并且，两个都是True结果才是True,只要有一个是False结果为False
print(True and True)
print(True and False)
#要求绩点3.5以上操评分90以上
score = 4.0
score2 = 80
print(score>3.5 and score2 >=90)
# or(或) --或者
#只要有一个是True结果都是True，两个都是False结果才是False,两个或者多个条件只要一个满足就行
print(score>3.5 or score2 >=90)

# not(非)
# True变成False,False变成True
age = 20
print(not age < 18)
