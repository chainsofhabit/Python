# 5.运算符的优先级
#正负 > 数学运算符 > 比较运算符 > 逻辑运算符 > 赋值运算符
#数学运算符中:**>(*,/,%,//)>(+ -)
#优先级高的先计算，如果优先级相同的就从左往右依次计算，可以通过括号改变运算顺序
print(10 * 20 + 30 < 40 / 2 - 100)
print(10 * (20 + 30) < (40 / 2 - 100))
print(2 * 2 ** 3)
print(10/4+23//4*2-30)