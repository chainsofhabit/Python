#练习：生成一个随机数，如果是偶数，打印偶数，否则打印奇数
#如果能够被4整除再打印能被4整除
# import是python中导入模块或者模块内容中的关键字
# random是python内置的产生随机数的模块
import random
number = random.randint(0,100) 
#产生一个0到100的随机整数并且存到number里
if number%2 == 0:
	if number%4 == 0:
		print('能被4整除')
	else:
		print('不能被4整除')
	print('偶数')
else:
	print('奇数')
print('随机数是:%d' %(number))
