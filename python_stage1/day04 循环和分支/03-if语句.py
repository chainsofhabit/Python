#python中的分支结构只有一种：if分支结构
"""
1.if
基本语法结构：
if 条件语句：
	执行语句块
说明：
a.if:python中的关键字，'如果'的意思，用来做判断
b.条件语句：最终的结果会被转换成bool值
c.冒号：固定写法，必须写！！
d.执行语句块：这可以是多行语句，但是每行语句必须和前面的if保持一个缩进(一个tab)

执行过程：先判断条件语句的结果是否为True，如果为True，就执行冒号后面的执行语句块，否则直接执行if模块后的其他语句

"""
age = 20
if age >= 18:
	print('成年')
	print('洗洗睡')
#用一个变量保存一个学生的成绩，要求一个学生的成绩大于90的时候打印优秀
#不管成绩是多少都把成绩打印出来
grade = 98
print(grade)
if grade > 90:
	print('nice')

'''
2.if-else
语法：
if 条件语句：
	执行语句块1
else:
	执行语句块2
其他语句
说明：
else:关键字(else后面的冒号不能省)
执行过程：先判断条件语句的结果是否成立，如果成立就执行语句块1，
		执行完语句块1后再执行其他语句；如果条件不成立就执行语句块2；
		执行完语句块2后再执行其他语句
'''
#要求年龄大于等于18打印成年
age2 = 17
if age2 >= 18:
	print('成年')
else:
	print('未成年')

"""
if-elif-else
语法：
if 条件语句1：
	语句块1
elif 条件语句2：
	语句块2
elif 条件语句3：
	语句块3
	....
else:
	语句块n
其他语句

"""
#成绩大于90打印优秀，70-90打印良好，60-69
age3 = 59
if age3 >= 90:
	print('优秀')
elif age3 >=70 :
	print('良好')
elif age3 >=60 :
	print('及格')
else:
	print('不及格')

